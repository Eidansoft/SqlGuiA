    private void btnConectaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectaActionPerformed
        ConfiguracionBD conf = new ConfiguracionBD();
        conf.setIp(txtHost.getText());
        conf.setPuerto(txtPort.getText());
        conf.setPw(txtPw.getText());
        conf.setSID(txtSid.getText());
        conf.setUsuario(txtUsuario.getText());
        conf.setNombre(txtNombre.getText());
        
        if (!configuraciones.contains(conf)){
            // Añade y guarda
            configuraciones.add(conf);
            guardaArchivoConfiguracion();
        }
        
        configuracionLista(conf);
        setVisible(false);
    }//GEN-LAST:event_btnConectaActionPerformed

    private void lstConexionesPreviasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstConexionesPreviasMousePressed
        int selRow = lstConexionesPrevias.getSelectedIndex();
        if(selRow != -1) {
            ConfiguracionBD c = configuraciones.get(selRow);
            
            if(evt.getClickCount() == 1) {
                if (evt.getButton() != MouseEvent.BUTTON1){ //Si haces clic con un boton que no sea el derecho
                    int dialogResult = JOptionPane.showConfirmDialog (null, "Elimino la configuracion '" + c.getNombre(),"Warning", JOptionPane.YES_NO_OPTION);
                    if(dialogResult == JOptionPane.YES_OPTION){
                        configuraciones.remove(c);
                        guardaArchivoConfiguracion();
                        actualizaListaConfiguraciones();
                    }
                }
            }
            else if(evt.getClickCount() == 2) {
                txtHost.setText(c.getIp());
                txtPort.setText(c.getPuerto());
                txtPw.setText(c.getPw());
                txtSid.setText(c.getSID());
                txtUsuario.setText(c.getUsuario());
                txtNombre.setText(c.getNombre());
            }
        }
    }//GEN-LAST:event_lstConexionesPreviasMousePressed

