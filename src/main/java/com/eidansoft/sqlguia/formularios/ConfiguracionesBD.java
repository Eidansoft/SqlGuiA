    private void guardaArchivoConfiguracion() {
        XStream xstream = new XStream(new DomDriver());
        xstream.alias("configuracion", ConfiguracionBD.class);
        String xml = xstream.toXML(configuraciones);
        StringBuilder sb = new StringBuilder();

            sb.append("<?xml version=\"1.0\"?>\n"); //write XML header, as XStream doesn't do that for us
            sb.append(xml);
            FuncionesComunesDeArchivos.guardaArchivo(archivoConf, sb.toString());
    }

    private void actualizaListaConfiguraciones() {
        lstConexionesPrevias.removeAll();
        DefaultListModel<ConfiguracionBD> listaConfiguraciones = new DefaultListModel<ConfiguracionBD>();
        Iterator<ConfiguracionBD> it = configuraciones.iterator();
        while (it.hasNext()) {
            ConfiguracionBD configuracionBD = it.next();
            listaConfiguraciones.addElement(configuracionBD);
        }
        lstConexionesPrevias.setModel(listaConfiguraciones);
        lstConexionesPrevias.updateUI();
    }
